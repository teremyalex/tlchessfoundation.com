                    <div style="background: white; margin: 20px 0; padding: 10px 20px; border-radius: 10px; box-sizing: border-box;">
                        <h3>The History of Men's World Chess Championships Before FIDE</h3>

                        <p>During the period before FIDE, the history of men's world chess championships is fascinating as it laid the foundation for modern world chess championships. The first official championship was held in 1886, but the concept of a world championship and the organization of matches were largely in the hands of the players themselves, rather than being overseen by an official organization like FIDE.</p>

                        <h3>The First World Championship (1886)</h3>
                        <p>The first official world chess championship match took place in 1886 between <strong>Wilhelm Steinitz</strong> and <strong>Johannes Zukertort</strong>. Steinitz, who had long been considered one of the world's strongest players, claimed the first official world title, becoming the first official world chess champion in history.</p>
                        <ul>
                            <li><strong>Champion:</strong> Wilhelm Steinitz</li>
                            <li><strong>Challenger:</strong> Johannes Zukertort</li>
                            <li><strong>Location:</strong> USA (New York, Saint Louis, New Orleans)</li>
                            <li><strong>Result:</strong> Steinitz won 12.5-7.5</li>
                        </ul>

                        <p>Wilhelm Steinitz (1886–1894) dominated chess after winning the world championship and played several title defense matches. Among his most famous challengers were <strong>Mikhail Chigorin</strong> and <strong>Isidor Gunsberg</strong>. Steinitz successfully defended his title against Chigorin in 1889 and then against Gunsberg in 1890–1891.</p>

                        <p>Steinitz's reign ended when <strong>Emanuel Lasker (1894–1921)</strong> defeated him in 1894, claiming the world title. Lasker held the title for a record 27 years, a record in chess history that remains unmatched. Lasker defended his title against many challengers, including great players like <strong>Frank Marshall</strong>, <strong>Siegbert Tarrasch</strong>, and <strong>Carl Schlechter</strong>.</p>
                        <p>The 1910 match between Lasker and Schlechter is particularly memorable, as it ended in a draw, allowing Lasker to retain his title under the rules. In 1921, <strong>José Raúl Capablanca</strong> finally defeated Lasker, taking over the world championship.</p>

                        <p><strong>José Raúl Capablanca (1921–1927)</strong>, the Cuban chess prodigy, was known for his extraordinary talent, characterized by simplicity and flawless play. He won the world title by defeating Lasker in 1921. Capablanca won numerous prestigious tournaments and is regarded as one of the greatest players in chess history.</p>

                        <p><strong>Alexander Alekhine (1927–1937)</strong> claimed the world title in 1927 by defeating Capablanca, marking a significant turning point in chess history. Alekhine was an ambitious and creative player who elevated chess to new heights. The rivalry between Alekhine and Capablanca is one of the greatest chapters in the history of world championships, although Alekhine never granted Capablanca a rematch.</p>
                        <p>Alekhine lost the title in 1935 to <strong>Max Euwe</strong> but regained it in 1937, holding it until his death in 1946.</p>

                        <p>The world championships before FIDE were highly personal events, where titleholders often selected their challengers, and organizing matches often required significant financial backing. Iconic players like Wilhelm Steinitz, Emanuel Lasker, José Raúl Capablanca, and Alexander Alekhine were at the center of these events, contributing significantly to the development and popularization of chess worldwide. After the establishment of FIDE, these matches became more formalized, but the legacy of the early world championships remains a defining part of chess history.</p>
                    </div>

                    <div style="background: white; margin: 20px 0; padding: 10px 20px; border-radius: 10px; box-sizing: border-box;">
                        <h3>FIDE World Chess Championships (1948-1993)</h3>

                        <p>The FIDE (International Chess Federation) took over the organization of the men's world chess championships in 1948, following the sudden death of the reigning world champion, Alexander Alekhine, in 1946. Alekhine's death left the chess world without an official world champion, prompting FIDE to initiate the first official world championship tournament to resolve the situation.</p>

                        <p>The first FIDE-organized world championship was held in 1948 in The Hague and Moscow. Five outstanding chess players participated in this event: <strong>Mikhail Botvinnik</strong>, <strong>Vasily Smyslov</strong>, <strong>Paul Keres</strong>, <strong>Samuel Reshevsky</strong>, and <strong>Max Euwe</strong>. <strong>Mikhail Botvinnik</strong> won the tournament, becoming the first official world champion appointed by FIDE, marking the beginning of the modern world championship era.</p>

                        <p>The new system introduced by FIDE for obtaining the world title involved several stages. First, zonal tournaments were held, whose winners advanced to the interzonal tournaments. The best from these tournaments then competed in the Candidates Tournament, which determined the challenger for the world champion. This system ensured that the fight for the world title was organized and fair, unlike the earlier period when titleholders could choose their challengers.</p>

                        <p><strong>Botvinnik</strong>'s dominance in chess during the 1950s was particularly significant. Although he lost the world title twice - first to <strong>Vasily Smyslov</strong> in 1957, and then to <strong>Mikhail Tal</strong> in 1960 - he successfully regained it both times in rematch matches. However, Botvinnik's reign ended in the early 1960s when <strong>Tigran Petrosian</strong> defeated him in 1963.</p>

                        <p><strong>Petrosian</strong>'s reign was relatively short, as <strong>Boris Spassky</strong> dethroned him in 1969. Spassky's victory further cemented Soviet dominance in chess, but this dominance was soon interrupted when American <strong>Bobby Fischer</strong> defeated Spassky in 1972. Fischer's triumph was of enormous significance as he became the first American to win the world title, breaking the Soviet hold that had lasted for decades.</p>

                        <p>After Fischer's withdrawal, FIDE appointed a new world champion: <strong>Anatoly Karpov</strong> assumed the title in 1975 after Fischer refused to defend it. Karpov dominated the chess world for a long time, until the mid-1980s. However, in 1985, <strong>Garry Kasparov</strong>, a new chess genius, defeated Karpov, ushering in the Kasparov era.</p>

                        <p>During Kasparov's reign, significant changes occurred in the chess world, including his breakaway from FIDE in 1993, which split the world championship title between FIDE and the Professional World Chess Championship. This period marked the end of the traditional FIDE world championship systems and the beginning of a new, challenging era in chess history.</p>
                    </div>

                    <div style="background: white; margin: 20px 0; padding: 10px 20px; border-radius: 10px; box-sizing: border-box;">
                        <h3>The Divided World Chess Championships (1993-2006)</h3>

                        <p>One of the most significant turning points in world chess championship history occurred in 1993 when <strong>Garry Kasparov</strong>, the reigning world champion, and <strong>Nigel Short</strong>, the then-challenger, broke away from the FIDE (International Chess Federation) and founded the Professional Chess Association (PCA). This initiated the division of the world championship title, which lasted for nearly a decade and a half.</p>

                        <p>From 1993 onwards, there were two separate world championship titles: the FIDE World Championship and the PCA-organized World Championship, later known as the Classical World Championship. <strong>Garry Kasparov</strong> defended his title under the PCA in 1993 against <strong>Nigel Short</strong> and in 1995 against <strong>Viswanathan Anand</strong>, while <strong>Anatoly Karpov</strong> retained the FIDE title until 1999, when <strong>Alexander Khalifman</strong> took over the throne at the FIDE knockout world championship.</p>

                        <p>During this period, the two world championship titles existed in parallel, and many top players became champions of both organizations. In the FIDE world championships, <strong>Vladimir Kramnik</strong> and <strong>Veselin Topalov</strong> emerged as prominent figures, while after Kasparov's retirement, Kramnik became the Classical World Champion.</p>

                        <p>The period of divided world championships finally ended in 2006 when <strong>Vladimir Kramnik</strong> and <strong>Veselin Topalov</strong> faced off in the "Unification World Championship Match." Kramnik's victory reunited the world championship title, making him the official world champion under FIDE once again.</p>
                    </div>

                    <div style="background: white; margin: 20px 0; padding: 10px 20px; border-radius: 10px; box-sizing: border-box;">

                        <h3>FIDE World Chess Championships 2006-Present</h3>

                        <p>After the 2006 unified world championship, where <strong>Vladimir Kramnik</strong> and <strong>Veselin Topalov</strong> faced off, with Kramnik emerging as the unified world champion, the chess world returned to a single world championship system. The FIDE (International Chess Federation) has since organized the world championship annually or biennially, where the world's best compete for the title.</p>

                        <p>In 2007, the FIDE world championship was held in a round-robin format in Mexico, where <strong>Viswanathan Anand</strong> emerged victorious and claimed the world title. Anand successfully defended his title against Kramnik in 2008, <strong>Veselin Topalov</strong> in 2010, and <strong>Boris Gelfand</strong> in 2012, solidifying his position at the top of the chess world.</p>

                        <p>In 2013, Norwegian <strong>Magnus Carlsen</strong> challenged Anand and defeated him, winning the world title. Since then, Carlsen has dominated the chess world, successfully defending his title multiple times. He won against Anand again in 2014, <strong>Sergey Karjakin</strong> in 2016, and <strong>Fabiano Caruana</strong> in 2018. In 2021, Carlsen defended his title again, this time against <strong>Ian Nepomniachtchi</strong>.</p>

                        <p>The 2023 world championship was special as Carlsen decided not to defend his title. As a result, Russian <strong>Ian Nepomniachtchi</strong> and Chinese <strong>Ding Liren</strong> competed for the world championship. Ding Liren emerged victorious, becoming China's first male world chess champion.</p>

                        <p>The FIDE-organized world championships remain among the most important events in the chess world and represent the highest level of chess play.</p>
